/**
 * Énumération des différentes couleurs valides pour l'affichage des notifications toast
 */
export enum EToastColor {

    /**
     * Couleur primaire (par défaut)
     */
    PRIMARY = 'primary',

    /**
     * Couleur secondaire
     */
    SECONDARY = 'secondary',

    /**
     * Couleur tertiaire
     */
    TERTIARY = 'tertiary',

    /**
     * Couleur de succès
     */
    SUCCESS = 'success',

    /**
     * Couleur d'avertissement
     */
    WARNING = 'warning',

    /**
     * Couleur d'erreur
     */
    DANGER = 'danger',

    /**
     * Couleur claire
     */
    LIGHT = 'light',

    /**
     * Couleur moyenne
     */
    MEDIUM = 'medium',

    /**
     * Couleur sombre
     */
    DARK = 'dark'
}
