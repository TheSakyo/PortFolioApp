/**
 * Énumération des différentes couleurs valides pour l'affichage des notifications toast
 */
export enum EToastColor {

    PRIMARY = 'primary', // Couleur primaire (par défaut)
    SECONDARY = 'secondary', // Couleur secondaire
    TERTIARY = 'tertiary', // Couleur tertiaire
    SUCCESS = 'success', // Couleur de succès
    WARNING = 'warning', // Couleur d'avertissement
    DANGER = 'danger', // Couleur d'erreur
    LIGHT = 'light', // Couleur claire
    MEDIUM = 'medium', // Couleur moyenne
    DARK = 'dark' // Couleur sombre
}